print('========= Operator Perbandingan =========')
print()
 
print('# Sama Dengan (==)')
a = 10 == 10
print('a:', a)
b = 10 == 20
print('b:', b)
print('3 == 3.1:', 3 == 3.1)
print('10 == 11:', 10 == 11)
print()
 
print('# Tidak Sama Dengan (!=)')
a = 10 != 10
print('a:', a)
b = 10 != 20
print('b:', b)
print('3 != 3.1:', 3 != 3.1)
print('10 != 10:', 10 != 10)
print()
 
print('# Lebih Kecil (<)')
a = 10 < 10
print('a:', a)
b = 1 < 5
print('b:', b)
print('9 < 10:', 9 < 10)
print()
 
print('# Lebih Besar (>)')
a = 10 > 10
print('a:', a)
b = 5 > 1
print('b:', b)
print('10 > 9:', 10 > 9)
print()
 
print('# Lebih Kecil atau Sama Dengan (<=)')
a = 10 <= 10
print('a:', a)
b = 5 <= 1
print('b:', b)
print('9 <= 10:', 9 <= 10)
print()
 
print('# Lebih Besar atau Sama Dengan (>=)')
a = 10 >= 10
print('a:', a)
b = 1 >= 5
print('b:', b)
print('10 >= 9:', 10 >= 9)
print()